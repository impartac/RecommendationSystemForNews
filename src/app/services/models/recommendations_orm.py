from src.app.services.models.base import Base

from sqlalchemy.orm import Mapped, mapped_column
from sqlalchemy import TEXT, DATETIME, ARRAY


class RecommendationsORM(Base):
    __tablename__ = 'news_recommendation'

    id: Mapped[TEXT] = mapped_column(TEXT, primary_key=True)
    recommendations: Mapped[ARRAY] = mapped_column(ARRAY(item_type=TEXT))
    updated_at: Mapped[DATETIME] = mapped_column(DATETIME)

    def __repr__(self):
        result = f"id: {str(id)}, recommendations: "
        for i in self.recommendations:
            result += f", {i}"
        return result

